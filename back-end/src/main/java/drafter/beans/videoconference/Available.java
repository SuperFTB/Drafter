package drafter.beans.videoconference;

public class Available {
	public String uuid;
	
	
	public Available() {
		super();
	}
	
	public Available(String uuid) {
		super();
		this.uuid = uuid;
		
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

}
